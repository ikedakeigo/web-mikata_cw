<h2>●記事のライティング</h2>
<ul class="check-list-">
	<li><label><input type="checkbox" /> 冒頭の導入部分はきっちり書く<br /></label></li>
	<li><label><input type="checkbox" /> 記事が長い場合は、まとめを書く<br /></label></li>
		<li><label><input type="checkbox" /> 思わずクリックしてしまうようなタイトルをつける<br /></label></li>
			<li><label><input type="checkbox" /> タイトルと記事の内容を一致させる<br /></label></li>
				<li><label><input type="checkbox" /> 冒頭ではなるべく結論から先に書く、コンテンツ内で結論に対する理由や根拠をかく<br /></label></li>
					<li><label><input type="checkbox" /> 逃げる表現は使わない　ex)「〜らしい」「だそうです」「だと思われます」<br /></label></li>
						<li><label><input type="checkbox" /> 無駄な表現は使わない　ex)「〜だということらしいです」「〜だと推測することができます」<br /></label></li>
							<li><label><input type="checkbox" /> 語尾はなるべく揃えない。　「です・ます」を多用しすぎない<br /></label></li>
								<li><label><input type="checkbox" /> 根拠がある場合は根拠を明記する<br /></label></li>
									<li><label><input type="checkbox" /> 引用する場合は引用元を明記する<br /></label></li>
									<li><label><input type="checkbox" /> 数字は半角で使用する<br /></label></li>
									<li><label><input type="checkbox" /> 同じ助詞を連続して使わない<br /></label></li>
									<li><label><input type="checkbox" /> 二重否定の表現は使わない<br /></label></li>
</ul>

<h2>●質の高い記事の特徴</h2>
<ul class="check-list-">
	<li><label><input type="checkbox" /> 自分が書いたことを自分自身が信じることができる</label></li>
	<li><label><input type="checkbox" />同じ内容の記事が自分のサイト内に存在しない</label></li>
	<li><label><input type="checkbox" /> サイトや記事のターゲット像がはっきりとわかる</label></li>
	<li><label><input type="checkbox" /> 他のサイトにない情報が載っている</label></li>
	<li><label><input type="checkbox" /> 記事に感情が込められている</label></li>
	<li><label><input type="checkbox" /> 書き手の人物像がはっきりとわかる</label></li>
	<li><label><input type="checkbox" /> 賛成意見（メリット）・反対意見（デメリット）の両面が書かれている</label></li>
	<li><label><input type="checkbox" /> 当たり前のことに関する自分なりの意見が書かれている</label></li>
	<li><label><input type="checkbox" /> サブヘッドがあり、目に留まりやすい工夫がされている</label></li>
	<li><label><input type="checkbox" /> 伝えたいことを伝える工夫ではなく、伝わる工夫がされている</label></li>
		<li><label><input type="checkbox" /> （情報量が多い記事では）最後にまとめが書かれている</label></li>
			<li><label><input type="checkbox" /> 無駄な文字数稼ぎをしていない</label></li>
				<li><label><input type="checkbox" /> 適度に画像が表示されている</label></li>
					<li><label><input type="checkbox" /> 図や表でうまくまとめられている</label></li>

</ul>

